package basic.exercise1;

/**
 * Given 2 numbers, show the sum, subtraction, multiplication and division of that numbers
 */
public class Exercise1 {

    public static void main(String[] args) {

        int number1 = 6;
        int number2 = 4;

        System.out.println("The sum of both numbers are: " + (number1 + number2));
        System.out.println("The subtraction of both numbers are: " + (number1 - number2));
        System.out.println("The multiplication of both numbers are: " + (number1 * number2));
        System.out.println("The division of both numbers are: " + (number1 / number2));

    }
}
